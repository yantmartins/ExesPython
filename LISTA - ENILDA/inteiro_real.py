num1 = int(input("Digite um número inteiro: "))
num2 = int(input("Digite outro número inteiro: "))
num3 = float(input("Digite um número real: "))

quest1 = (num1 * 2) * (num2 / 2)
print(f"O produto do dobro do primeiro número com metade do segundo número é: {quest1} ")

quest2 = num1 * 3 + num3
print(f"A soma do triplo do primeiro com o terceiro é: {quest2}")

quest3 = num3 ** 3
print(f"O terceiro número elevado ao cubo é: {quest3}")
